<font color="#800000"><b>President's Tool Series (Self-developed Package) </b></font>

President's Tool Series is developed software
with various ideas which have been compiled in
our company so far. This is exactly what people
want because it is proved with actual practice
and based on the viewpoints of a manager and
a supervisor.

<p ><font color="#800000"><b>(1) Attendance Management System</b></font>
   All the labor management can be unified for official
   employees, contract employees,temporary staffs,
   and dispatched workers. Since working time can be
   set for not only each individual pattern but also
   daily irregular patterns, this will make it easier
   for you to calculate salary or a dispatch charge.

   An operator can input and apply for working
   conditions from anywhere in the world as long as
   one has a network environment. Then, all kinds of
   labor management of a paid holiday, overtime work,
   holiday work, as well as working hours is capable
   with the internet. Of course, smartphone input
   is available.</p>

<p><font color="#800000"><b>(2) Stock Management System</b></font>
   Most of unlisted companies are family companies.
   The investment in your company is probably made
   by families, relatives, friends, and executives
   by themselves. Stock transfer (trade) among these
   people concerned is ruled strictly by tax law.
   You don’t want to pay it if possible. 
   
   "How much can we make a trade at, then?"
   This system answers the question. When issuing
   a new stock, a "present" reasonable price
   turns out by controlling time-series data such as
   an accepted price, a sales price, and a purchase
   price. This system is essential for owners.</p>

<p><font color="#800000"><b>(3) Cost Estimation System</b></font>
   "Sales of service" including labor costs requires
   understanding of specific cost. This automatically
   calculates salary, overtime work, a bonus, social
   insurance premium,and labor insurance premium,
   in addition to a transportation cost, and then
   figures out the cost price. By using this, a cost
   rate is calculated with assumed working hours of
   the upper limit and the lower limit based on
   monthly regular-working hours, which is very
   appreciated for its clear profitability.</p>

<p><font color="#800000"><b>(4) Home Page System</b></font>
   Nowadays, every company has its own Home Page.
   Our HP System offers not only the contents open
   to the public which you are looking at now,
   but also the contents for employees.
   An announcement from a company to employees
   is made by the function of "announcement upload
   sub-system", which doesn’t need special technical
   knowledge and can be easily replaced and uploaded.
   As for the employees, they can download the necessar
   application form anytime with the internet at home,
   and in fact Attendance Management System can be used
   from the contents for employees.
   
   For example, if you can access the contents
   for employees at the time of a disaster,
   writing for safety confirmation is available to
   the specific pages from all over the world.
   You can add various functions corresponding
   with the needs in your company, and can make
   your own HP system.</p>

<p><font color="#800000"><b>(5) Skill-sheet Making System</b></font>
   It is essential to make a presentation of
   the staff with a skill sheet for
   an engineering-service company. Consequently,
   we have developed the system which can make
   multiple pages of clear skill sheets and save them
   to DB. </p>

<p><font color="#800000"><b>(6) Other Systems</b></font>
   We offer other systems as props such as "Estimate
   and Invoice Management System" which enables
   an input of fraction number calculated on a pro-rate
   basis with 5/30 man-month and the like,"Asset
   Management System", and "Contract-Register Management
   System" </p>

