ビスをしていますがこのサービス形態を「支援」とも言います。「支援」
の内容としては、新しいビジネスの企画・分析、開発・構築、検査・評価、
運営・保守までと幅広く、当社の<span style="font-family : 'century'">ＳＥ（System Engineer）</span>が多数活躍して
います。

