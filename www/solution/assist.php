<font color="#800000"><b>システム・エンジニアリング支援</b></font>

この事業は、<span style="font-family : 'century'">System Engineering Services（ＳＥＳ）</span>と言われるもので、
お客様に「製造品」は提供しませんが、ソフトウエアの開発やネットワー
ク・システムの構築をする、「技術的サービス」をお客様に提供するもの
です。

