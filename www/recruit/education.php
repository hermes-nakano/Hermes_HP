			当社は優秀な人材の育成とエンジニアの技術向上に積極的に取り組んでいます。特に、新卒者への入社前研修は約１０ケ月間に渡って実施されます。この間、先輩社員がマンツーマンで付き、様々な技術上の疑問についてレクチャーをしますので、入社直後は他社の新人と比べ最低でも１年先を行く実力が付くのです。<br><br>
			また、入社後も引き続き各種の研修を実施する他、社会人としての常識を身に付けるようにしています。例えば、法務局に行き商業登記簿謄本を取得したり、上場会社の定時株主総会に会社を代表して決議権の行使をしたり、業界団体の会合に出席して名刺交換をしたりします。<br><br>
			当社のシステム・エンジニアは社会や企業の基盤を裏方で支えるシステム開発やネットワーク構築を行なっていますので、特化した専門技能と幅広い社会常識がなければ正しい洞察も、見識も培われることはないと思っております。<br><br>
			また、社外教育機関によるセミナーや、資格取得の支援制度を整え、社員のスキルアップを強力に支援しています。さらに、社員の国際感覚を養うために、可能な限り海外出張の機会を与えております。<br><p>
